// 6.Write a program to print even number between 10 and 100 using while
public class even_while {
    public static void main(String args[]){
        int i=10;
        System.out.print(" even number between 10 and 100 \n");
        while(i<=100)
        {
            System.out.println(i);
            i=i+2;
        }
    }
}
