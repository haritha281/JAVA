// 6. Program for relational operators (<,<=, >, >=)
public class rel_op {
   public static void main(String[] args) {
       int a=5, b=11;
       // < operatir
       System.out.println(a<b);
       // <= operator
       System.out.println(a<=b);
       // > operator
       System.out.println(a>b);
       // >= operator
       System.out.println(a>=b);
   } 
}
